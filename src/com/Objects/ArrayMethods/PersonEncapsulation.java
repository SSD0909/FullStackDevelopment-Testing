package com.Objects.ArrayMethods;

public class PersonEncapsulation {

	private String name;
	private int age;
	
	public void SetName(String name) {
		
		this.name=name;
	}
public void SetAge(int age) {
		
		this.age=age;
	}
public String getName() {
	
	return this.name;
}
	
public int getAge() {
	
	return this.age;
}	
}
